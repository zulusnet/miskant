		<h3>Vorbereitung des Bodens.</h3>
		<p class="after-header">Vor der Gründung der Plantage (im Herbst ein
			Jahr vor der Pflanzung ) sollte man, um den Boden für Bepflanzung
			vorzubereiten, folgendes tun :</p>
		<ol class="list square" style="margin-bottom: 25px">
			<li>Das Besprühen des Bodens mit Pflanzenschutzmitteln (Round`up),
				ist bei Einrichtung der Plantage in Wiesen und Brachland empholen</li>
			<li>Nach der Unkraut -bekämpfung, sollte eine vertieftes Pflügen
				(20-25 cm) tun.</li>
		</ol>
		<p>Im frühen Frühling vor der Pflanzung empfehlt sich :</p>
		<ol class="list square" style="margin-bottom: 25px;">
			<li><strong>Güllen und bröckeln des Bodens (Aggregation,
					Erschütterung)</strong></li>
		</ol>
		<h3>Pflanzung.</h3>
		<p class="after-header">Das Miscanthus X Giganteus pflanzen wir mit
			Wurzelpflänzlinge aus den landlichen Plantagen an.</p>
		<p>Die Zeit der Anpflanzung ist eng mit Witersaizon verbunden. Die
			Anpflanzung soll anfangs des Aprils bis zum Ende des Mai durchgeführt
			warden. Aber in Praktik ist es von Wetter abhängig. Aus Erfahrung
			wissen wir, dass die Pflanzung des Miscanthux X Giganteus nach den
			endgültigen Ende der Frőste mőglich ist. Kann aber bis wir die ersten
			Triebe, der schon angepflantzten Plantage merken.</p>
		<h3>Pfläge. Erhalt der Platage.</h3>
		<p class="after-header">Der Anbau im ersten Jahr von den Miscanthus X
			Giganteus begrenzt sich nur zum Schutz der Plantage
			(Unkrautbekämpfung), wenn die jungen Pflanzen in der Lage nicht sind,
			selbst um die Sonne und Mineralstoffen mit stärkeren Unkraut zu
			kämpfen. Vor dem Abwuchs des Miscanthus X Giganteus kőnnen wir ohne
			Angst das Schutzmittel Sorte RoundUp nützen.</p>

		<p>Aus Erfährung wissen wir, dass solche Behandlung an der Plantagen
			reicht, die auf der IV Klasse und besseren Bőden enstanden sind. Die
			Situation ändert sich, wenn wir mit wenig fruchtbaren Bőden zu tun
			haben. Dann kőnnen wir den Miscanthus X Giganteus beihelfen:</p>
		<ol class="list square">
			<li>Zum Bekämpfung der zweiblättrigen Unkraut CHWASTOX, LINTUR,
				GRANSTAR, MUSTANG nützen;</li>
			<li>Zum Bekämpfung der einblättrigen Unkraut (Quetche, Hafer) die
				Mittel TITUS und ATTRIBUT nützen.</li>
		</ol>
