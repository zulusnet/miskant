	<p>Wir wollen an diesem Ort informieren, dass  im Saison 2013 das Unternehmen DORAN, als erste im Land, hat ein Projekt für Annhme einer Pilotplantage Riesen-Chinaschilf in der Ukraine durchgeführt. Diese Aufgabe wurde unter Vertrag mit der ukrainischen Gruppe KSG Agro SA realisiert, die an der Börse in Warschau notiert ist.</p>
	<p>Die erfolgreiche Umsetzung des Vertrages hat sich mit der Erfüllung der Bedingungen verbunden, die in der Ukraine Pflanzenschutzbedingungen gelten . Deshalb unsere Plantage wurde  mit Einzelheiten überprüft, falls des Auftretens der folgenden Pilzerkrankungen und Schädlinge:</p>
	<ol class="list square">
	<li>Synchytriumendobioticum - <strong>Kartoffelkrebs</strong></li>
	<li>Globoderaspp. - <strong>Nematoden</strong></li>
	<li><strong>Ditylenchusdipsaci</strong></li>
	<li>Meloidogynefallax - <strong>Dutch Wurzelgallennematoden</strong></li>
	</ol>
	<p>
	Laboranalysen von der Landesaufsichtsbehörde für Pflanzenschutz und Saatgut in Bydgoszcz, zeigten deutlich, dass die von <strong>uns führte Plantage ist frei von den oben erwähnten Pilze, Krankheiten und Schädlinge</strong>. Dank der durchgeführten, in diesem Jahr, Forschungen, können wir mit Erfolg und Garantie, unsere Sämlinge ausländischen Kunden anbieten, auch außer der Europäischen Union.
	</p>
