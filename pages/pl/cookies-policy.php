<p>Wykorzystujemy pliki cookies w celu:</p>
<ul class="list square" style="margin-bottom: 25px;">
	<li>dostosowania serwisu do preferencji użytkowników,</li>
	<li>pozyskiwania anonimowych danych dotyczących sposobu, w jaki użytkownicy korzystają z serwisu,</li>
	<li>ułatwienia użytkownikom poruszania się po stronach serwisu i korzystania z niego,</li>
	<li>zapewnienia standardów bezpieczeństwa.</li>
</ul>
<p>Stosujemy pliki cookies „stałe” oraz „sesyjne”. Pliki cookies „stałe” pozostają w przeglądarce internetowej urządzenia do czasu ich usunięcia przez użytkownika bądź do z góry ustalonego czasu określonego w parametrach pliku cookies. “Sesyjne” pliki cookies pozostają w przeglądarce do momentu jej wyłączenia lub wylogowania się ze strony internetowej na której zostały zamieszczone.</p>
<p>Pliki cookies wykorzystywane są również przez podmiot współpracujący z Andrzej Rogulski DORAN – Google Inc. Wykorzystujemy usługi Google Analytics oraz YouTube, które legitymują się własną polityką prywatności:</p>
<ul  class="list square"  style="margin-bottom: 25px;">
	<li>Polityka ochrony prywatności Google Analytics - <a href="http://www.google.pl/intl/pl/analytics/privacyoverview.html" target="_blank">http://www.google.pl/intl/pl/analytics/privacyoverview.html</a></li>
