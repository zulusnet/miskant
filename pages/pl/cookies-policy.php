		<h1>Polityka cookies</h1>
		<p class="after-header">Opisana polityka plików cookies odnosi się do wszystkich stron internetowych oraz aplikacji na urządzenia mobilne, należących do Andrzej Rogulski DORAN.</p>
<blockquote class="pull-right">
	<p>Ciasteczka (ang. cookies) - niewielkie informacje tekstowe, wysyłane przez serwer WWW i zapisywane po stronie użytkownika (zazwyczaj na twardym dysku). Domyślne parametry ciasteczek pozwalają na odczytanie informacji w nich zawartych jedynie serwerowi, który je utworzył. Ciasteczka są stosowane najczęściej w przypadku liczników, sond, sklepów internetowych, stron wymagających logowania, reklam i do monitorowania aktywności odwiedzających.</p>
