		</ol>
		<p>Wczesną wiosną, przed wysadzeniem roślin zaleca się:</p>
		<ol class="list square" style="margin-bottom: 25px;">
			<li><strong>Wysiać nawóz oraz spulchnić glebę (agregowanie, bronowanie)</strong></li>
