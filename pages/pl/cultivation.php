		<h3>Sadzenie.</h3>
		<p class="after-header">Sadzenia miskanta dokonujemy przy użyciu
			sadzonek korzeniowych (rhizomów) pochodzących z krajowych plantacji.</p>
		<p>Termin sadzenia jest ściśle uzależniony od sezonu zimowego. W
			teorii przyjmuje się iż sadzenia należy dokonywać od początku
			kwietnia do końca maja. W praktyce jednak <strong>jedynym determinantem jest
			pogoda.</strong> Z doświadczenia wiemy, że sadzenia miskanta możemy dokonywać
			po definitywnym ustaniu mrozów, i może ono trwać aż do czasu gdy
			zauważamy pierwsze odrosty w wysadzonych już plantacjach.</p>
		<h3>Pielęgnacja, utrzymanie plantacji.</h3>
		<p class="after-header">Przy uprawie Miskanta zabiegi pielęgnacyjne
			ograniczają się do ochrony plantacji (odchwaszczania roślin) w
			pierwszym roku wegetacji, gdy młode to jeszcze rośliny nie są w
			stanie wygrać walki o słońce i minerały z mocniejszymi chwastami.
			Przed wschodem miskanta bez obawy możemy zastosować środek typu
			RoundUp (Randap).</p>
		<p>Z doświadczenia wiemy, że taki zabieg jest wystarczający, w
			przypadku plantacji zakładanych na glebach klasy IV i lepszych.
			Sytuacja wygląda trochę inaczej, gdy mamy do czynienia z glebami
			gorszej jakości. Wtedy możemy wspomóc miskanta poprzez zastosowanie
			następujących środków:</p>
		<ol class="list square">
			<li>do zwalczania chwastów dwuliściennych można stosować CHWASTOX,
				LINTUR, GRANSTAR, MUSTANG</li>
			<li>do zwalczania chwastów jednoliściennych (perz, owies głuchy,
				chwastnica, włośnice) środki o nazwie TITUS i ATTRIBUT.</li>
		</ol>
