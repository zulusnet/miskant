	<div id="main-content">
		<h3>Preparing the soil.</h3>
		<p class="after-header">PPrior to founding the plantation is worth do
			the following (in autumn of the year preceding planting) in order to
			prepare the ground for planting:</p>
		<ol class="list square" style="margin-bottom: 25px">
			<li>In the case of plantation establishment on <strong>meadows and
					wastelands it is recommended to spray the soil with Roundup</strong>
				or other herbicides</li>
			<li>After weed perform in-depth (20 - 25 cm) followed by a dose of
				plowing fertilizer, eg organic fertilizer.</li>
		</ol>
		<p>In early spring, before planting crops it is recommended to:</p>
		<ol class="list square" style="margin-bottom: 25px;">
			<li><strong>Sow manure and crumble the soil (aggregation, harrowing)</strong>
			</li>
		</ol>
		<h3>Planting.</h3>
		<p class="after-header">Planting of Miscanthus is made by using root
			cuttings (rhizomes) derived from domestic plantations.</p>
		<p>
			Deadline for planting is strictly dependent on the winter season. The
			theory assumed that the planting should be made from early April to
			late May. In practice, however, <strong>the only determinant is the
				weather</strong>. We know from experience that the planting of
			Miscanthus can be made after the definitive cessation of frosts, and
			it may take until the time that we notice first offshoots in already
			planted plantations.
		</p>
		<h3>Care, maintenance of the plantation.</h3>
		<p class="after-header">During Miscanthus cultivation beauty
			treatments are limited to the protection of plantations (weed plants)
			in the first year of vegetation when young plants are still not able
			to win the fight for sun and minerals with stronger weeds. Before
			emergence of Miscanthus we can use, without fear, Roundup-type
			measure.</p>
		<p>We know from experience that such treatment is sufficient for the
			intended plantation on soils class IV and better. The situation looks
			a little bit different when we deal with lower quality soils. Then we
			can help Miscanthus by applying the following measures:</p>
		<ol class="list square">
			<li>For dicotyledonous weed control may be applied Chwastox, LINTUR,
				GRANSTAR, MUSTANG</li>
			<li>To control monocot weeds (Elytrigia,Avena fatua, Echinochloa,
				Setaria) chemical agents: TITUS and ATTRIBUT</li>
		</ol>
