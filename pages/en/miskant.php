		<p>
			The <strong>main characteristics</strong> of Miscanthus are:
		</p>
		<ul class="list decimal" style="margin-bottom: 25px;">
			<li>rapid growth (especially visible in hot years);</li>
			<li>high biomass yield per unit of area;</li>
			<li>low soil requirements (well-behaving, even on soils of class V
				and VI)</li>
		</ul>
		<p>
			In Polish conditions, the most important (critical) moment of
			vegetation is the first year , in which still weak seedlings are
			exposed to low temperatures. <strong>Protection here is helpful, for
				example by mulching</strong>. Miscanthus achieves its full potential
			in the third vegetation season, so special attention should be paid
			at careful preparation of soil before planting crop
		</p>
		<p>
			Species of Miscanthus x giganteus (specie triploidalny) is sterile.
			This means that it does not produce seeds through generative
			reproduction, <strong>can be reproduced by division of the
				underground rhizomes</strong> or from cuttings obtained by using “in
			vitro” cultures.
		</p>
		<p>
			Productivity of Miscanthus giganteus cultivation is from about <strong>10
				to 30 tonnes of dry mass per hectare</strong>, <strong>calorific
				value</strong> ranges from <strong>14 to 17 MJ per kilo</strong>.
			Durability of plantation is defined for about 15 years.
		</p>

